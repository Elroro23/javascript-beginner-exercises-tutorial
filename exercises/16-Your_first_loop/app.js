//Definimos un bucle "for".
for (let i = 0; i <= 100; i++) {
	console.log(i)//Imprimimos el indice del bucle(números del 0 al 100).
}
//let i = 0; (i es igual a 0 así inicializamos el contador desde el 0).
//i <= 100; (Es la condición, el bucle se ejecuta mientras i sea menor o igual a 100).
//i++ (El incremento del bucle será de 1 por cada iteración).